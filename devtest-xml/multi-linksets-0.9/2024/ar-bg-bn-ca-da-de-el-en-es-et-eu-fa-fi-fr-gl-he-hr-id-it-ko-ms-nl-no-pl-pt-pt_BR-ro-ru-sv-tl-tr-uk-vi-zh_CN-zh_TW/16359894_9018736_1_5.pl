Czasem wydaje nam się , że zawsze będziemy sami . I że tylko cierpienie dotrzyma nam towarzystwa . 
Wmawiamy sobie , że problem leży w nas . 
Zwłaszcza , gdy na drodze stanie nam coś , nad czym nie mamy władzy . 
Dlatego trzeba porzucić żal i cierpienie . I pamiętać , że tak naprawdę walczymy o naszych bliskich . 
Niech będzie to światłem , które poprowadzi cię w najniebezpieczniejszych chwilach . I w najciemniejszym mroku . 
